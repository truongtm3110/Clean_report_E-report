def string_to_price_format(str):
    if str is None:
        return str
    return '{:0,.0f}'.format(float(str))


def string_to_number_float(string):
    number_have_comma = re.sub(r'[^\d.,]+', '', string)
    number_float = re.sub(r'[^\d.]+', '', number_have_comma)
    return float(number_float)


